    private void tblGroupsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGroupsMouseClicked
        // TODO add your handling code here:
        if(this.tblGroups.getSelectedRowCount() > 0){
            this.jTabbedPane1.setEnabledAt(0, false);
            this.jTabbedPane1.setEnabledAt(1, false);
            this.jTabbedPane1.setEnabledAt(2, true);
            int index=this.tblGroups.getSelectedRow();
            res.Group group=res.CommRes.groups.get(index);
            this.lblGrpId.setText(index + "");
            this.lstActive.removeAll();
            this.lstGrpMembers.removeAll();
            boolean member=false;
            Vector<String> listData=new Vector<String>();
            this.btnSendReq.setVisible(true);
            for(int i=0;i<group.active.size();i++){
                if(group.active.get(i).id.equals(res.CommRes.id)){
                    member=true;
                    this.jTabbedPane1.setEnabledAt(0, true);
                    this.jTabbedPane1.setSelectedIndex(0);
                    this.btnExit.setVisible(true);
                    this.btnSendReq.setVisible(false);
                    DefaultTableModel tmodel=(DefaultTableModel)this.tblMsg.getModel();
                    tmodel.setRowCount(0);
                    for(int j=0;j<group.chat.size();j++){
                        this.updateChatTable(group.chat.get(j));
                    }
                    
                }
                listData.add(group.active.get(i).name);
                this.txtAdmin.setText(group.admin.name);
                this.txtGroupTitle.setText(group.title);
                this.txtDescGroup.setText(group.desc);
                this.txtCreateOn.setText(group.created_on);
            }
            
            this.lstActive.setListData(listData);
            this.lstGrpMembers.setListData(listData);
            if(group.admin.id.equals(res.CommRes.id)){
                this.jTabbedPane1.setEnabledAt(1,true);
                this.lstRequest.removeAll();
                listData=new Vector<String>();
                for(int i=0;i<group.requests.size();i++){
                    ClientDetails details=group.requests.get(i);
                    listData.add(details.name);
                    
                }
                this.lstRequest.setListData(listData);
            }
            
            if(!member)
                this.jTabbedPane1.setSelectedIndex(2);
            
        }
        
    }//GEN-LAST:event_tblGroupsMouseClicked

