    void updateChatTable(ChatDetails cdetails) {
        DefaultTableModel tmodel=(DefaultTableModel)this.tblMsg.getModel();
        Vector<String> rowData=new Vector<String>();
        rowData.add(cdetails.name + "\n(" + cdetails.sent_on + ")");
        rowData.add(cdetails.content);
        tmodel.addRow(rowData);
        tmodel.fireTableDataChanged();
    }

