    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog (this, "Are you sure?","EXIT GROUP", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) { 
            boolean grp_member=false;
            boolean admin=false;
       
            int index=Integer.parseInt(this.lblGrpId.getText());
            res.Group group=res.CommRes.groups.get(index);
            if(group.admin.id.equals(res.CommRes.id))
                 admin=true;  
            else{
                for(int i=0;i<group.active.size();i++){
                    if(group.active.get(i).id.equals(res.CommRes.id)){
                            grp_member=true;  
                            break;
                    }
                }
            }
             
            if(admin){// If admin : delete group
                try{
                    ObjectOutputStream out=new ObjectOutputStream(res.CommRes.client.getOutputStream());
                    out.writeObject("GroupDel");
                    out.writeObject(index+"");
                    JOptionPane.showMessageDialog(this, "Group deleted successfully!!!","Group",JOptionPane.INFORMATION_MESSAGE);
                }catch(Exception ex){
            
                }  
            }
             
            if(grp_member){// If group member : delete member from active array list
                this.jTabbedPane1.setEnabledAt(0, false);
                this.jTabbedPane1.setSelectedIndex(2);
                try{
                    ObjectOutputStream out=new ObjectOutputStream(res.CommRes.client.getOutputStream());
                    out.writeObject("GroupExit");
                    out.writeObject(index+"");
                    JOptionPane.showMessageDialog(this, "Exited from group successfully!!!","Group",JOptionPane.INFORMATION_MESSAGE);
                }catch(Exception ex){
            
                }  
            }
             
        }                                       
    }//GEN-LAST:event_btnExitActionPerformed

