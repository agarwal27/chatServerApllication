    private void btnAttachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttachActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser=new JFileChooser();
        chooser.showOpenDialog(this);
        File file=chooser.getSelectedFile();
        if(file!=null){
            try {
                FileInputStream in=new FileInputStream(file);
                byte barr[]=new byte[(int)file.length()];
                in.read(barr);
                in.close();
                String fname=file.getName();
                ObjectOutputStream out=new ObjectOutputStream(res.CommRes.client.getOutputStream());
                out.writeObject("ChatMesgAttach");
                out.writeObject(barr);
                out.writeObject(fname);
                out.writeObject(this.lblGrpId.getText());
                
                
            } catch (Exception e) {
            }
            
            
        }
        
        
    }//GEN-LAST:event_btnAttachActionPerformed

    private void tblMsgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMsgMouseClicked
        // TODO add your handling code here:
        if(this.tblMsg.getSelectedRowCount() > 0){
            int row=this.tblMsg.getSelectedRow();
            int grpId=Integer.parseInt(this.lblGrpId.getText());
            Group group=res.CommRes.groups.get(grpId);
            if(group.chat.get(row).type.equals("media")){
                JFileChooser chooser=new JFileChooser();
                chooser.showSaveDialog(this);
                File file=chooser.getSelectedFile();
                if(file!=null){
                    try {
                        ObjectOutputStream out=new ObjectOutputStream(res.CommRes.client.getOutputStream());
                        out.writeObject("ChatData");
                        out.writeObject(grpId + "");
                        out.writeObject(row + "");
                        out.writeObject(file.getPath());
                        
                    } catch (Exception e) {
                    }
                    
                    
                }
            }
            
        }
    }//GEN-LAST:event_tblMsgMouseClicked

